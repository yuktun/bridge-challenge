const normalizedTranslations = Object.fromEntries(
  Object.entries(translations).map(([key, value]) => [key.replace(/\s+/g, " ").trim(), value])
);

const originalText = new WeakMap();
const skipTranslation = node => node.parentElement?.closest('.question-block');

function translateTextNode(node, language) {
  if (!node.nodeValue || skipTranslation(node)) return;
  if (!originalText.has(node)) originalText.set(node, node.nodeValue);
  const source = originalText.get(node);
  const trimmed = source.trim();
  if (!trimmed) return;
  const normalized = trimmed.replace(/\s+/g, " ");
  const translated = language === 'zh-HK' ? (translations[trimmed] || normalizedTranslations[normalized]) : null;
  const replacement = translated || trimmed;
  const nextValue = source.replace(trimmed, replacement);
  if (node.nodeValue !== nextValue) node.nodeValue = nextValue;
}

const originalAttributes = new WeakMap();

function translateAttributes(element, language) {
  if (!(element instanceof Element)) return;
  const attrs = ['placeholder', 'title', 'aria-label'];
  let saved = originalAttributes.get(element);
  if (!saved) {
    saved = {};
    originalAttributes.set(element, saved);
  }
  for (const attr of attrs) {
    if (!element.hasAttribute(attr)) continue;
    if (!(attr in saved)) saved[attr] = element.getAttribute(attr);
    const source = saved[attr];
    element.setAttribute(attr, language === 'zh-HK' ? (translations[source] || source) : source);
  }
}

function translateTree(root, language) {
  if (root.nodeType === Node.TEXT_NODE) return translateTextNode(root, language);
  if (root instanceof Element) translateAttributes(root, language);
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_ALL);
  let node;
  while ((node = walker.nextNode())) {
    if (node.nodeType === Node.TEXT_NODE) translateTextNode(node, language);
    else if (node.nodeType === Node.ELEMENT_NODE) translateAttributes(node, language);
  }
}

function applyLanguage(language) {
  const lang = language === 'zh-HK' ? 'zh-HK' : 'en';
  localStorage.setItem('bridgeLanguage', lang);
  document.documentElement.lang = lang;
  document.body.dataset.language = lang;
  translateTree(document.body, lang);
  document.querySelectorAll('[data-language-option]').forEach(button => {
    const active = button.dataset.languageOption === lang;
    button.classList.toggle('active', active);
    button.setAttribute('aria-pressed', String(active));
  });
  window.dispatchEvent(new CustomEvent('bridge-language-change', { detail: { language: lang } }));
}

window.bridgeI18n = {
  t(text) {
    return localStorage.getItem('bridgeLanguage') === 'zh-HK' ? (translations[text] || text) : text;
  },
  applyLanguage,
  get language() { return localStorage.getItem('bridgeLanguage') || 'en'; }
};

const observer = new MutationObserver(records => {
  const language = window.bridgeI18n.language;
  for (const record of records) {
    record.addedNodes.forEach(node => translateTree(node, language));
    if (record.type === 'characterData') translateTextNode(record.target, language);
  }
});

function initLanguage() {
  document.querySelectorAll('[data-language-option]').forEach(button => {
    button.addEventListener('click', () => applyLanguage(button.dataset.languageOption));
  });
  observer.observe(document.body, { childList: true, subtree: true, characterData: true });
  applyLanguage(localStorage.getItem('bridgeLanguage') || 'en');
}

if (document.readyState === 'loading') document.addEventListener('DOMContentLoaded', initLanguage);
else initLanguage();
